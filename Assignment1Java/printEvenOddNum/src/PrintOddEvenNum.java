package assign1;
public class PrintOddEvenNum {
    public static void main (String [] args)
    {
        System.out.println("The 10 even numbers starting from 0 are:");
        for(int i=0;i<=20;i+=2)
        {
            System.out.println(i);
        }
        System.out.println("The 10 odd numbers starting from 1 are:");
        for(int i=1;i<=20;i+=2)
        {
            System.out.println(i);
        }
    }
}